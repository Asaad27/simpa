package WSetFinder;

/**
 * Created by jean on 27/04/16.
 * Store an Input sequence and its answer
 */
public class IO {
    private String input;
    private String output;

    public IO(String input, String output) {
        this.input = input;
        this.output = output;
    }

    public String getInput() {
        return input;
    }

    public String getOutput() {
        return output;
    }
}
